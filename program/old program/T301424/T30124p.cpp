#include <iostream>
#include <cstdio>
using namespace std;

int main()
{
    int n;
    scanf("%d",&n);
    while(n--)
    {
        int k;
        scanf("%d",&k);
        if(k==1)
        {
            cout<<1<<endl;
        }
        else printf("-1\n");
    }
    return 0;
}