void solve() {
    int a, b, c, r; cin >> a >> b >> c >> r;
    if(a > b) swap(a, b);
    int ans = 0;
    ans += max(0, min(b, c - r) - a) + max(0, b - max((c + r), a));
    cout << ans << '\n';
}

